-- 분양(반려동물판매업) 체인 후보 추출 — 앱 신뢰도 점수제 화이트리스트 생성용
--
--   배경: 사업자 업종만 '반려동물판매업'(category='pet_sales')으로 등록하고 실제론
--   분양 전문이 아닌 업장(마트 펫코너·용품점·법인 등)이 섞여 있다. 앱은 상호명
--   키워드로 신뢰도를 점수화해 ⚠ 경고를 띄우는데(가점: 분양/켄넬/캐터…, 감점:
--   (주)/컴퍼니/홀딩스/용품…), (주) 등이 붙은 실제 분양 체인이 억울하게 감점되는
--   걸 막으려고 '예외 화이트리스트'를 둔다.
--
--   이 쿼리는 그 화이트리스트의 "후보"를 데이터에서 자동 추출한다. 같은 상호가
--   전국에서 자주 등장(≥4회)하면 체인으로 보고 후보에 올린다. 단, 빈도만으로는
--   대형마트·일반명사·지역명이 섞이므로 가드(아래 NOT IN/정규식)로 걸러낸다.
--
--   사용처: 앱 lib/services/facility_repository.dart 의 _salesWhitelist 갱신.
--   결과를 사람이 한 번 검토(수족관/법인 등 분양 아님 제외)한 뒤 코드에 반영한다.
--   데이터는 정적(공공데이터 1회 적재)이라 재적재 시에만 다시 돌리면 된다.
--
--   주의: 화이트리스트는 앱에서 '부분일치'로 매칭한다. 그래서 '캣플'만 넣어도
--   '캣플헤븐'이 포함되고, '야옹아'로 두면 '야옹아멍멍해봐펫'과 띄어쓰기 변형까지
--   모두 잡힌다. 코드에 넣을 땐 가능한 한 짧고 고유한 어간을 쓴다.
-- ============================================================================

with cleaned as (
  -- 법인 표기 제거: (주) / ㈜ / 주식회사
  select btrim(regexp_replace(name, '\(주\)|㈜|주식회사', '', 'g')) as t
  from public.facilities
  where category = 'pet_sales' and is_open
),
norm as (
  -- 첫 공백/괄호/슬래시 이후(지점·부가설명)를 잘라 핵심 상호 토큰만 남긴다.
  --   예) '도그마루 경인지점' -> '도그마루', '밀레펫(이마트 천안점)' -> '밀레펫'
  select regexp_replace(t, '[\s(/].*$', '') as brand from cleaned
),
agg as (
  select brand, count(*) as cnt
  from norm
  where length(brand) >= 2
  group by brand
  having count(*) >= 4          -- 체인 후보 임계값(전국 4회 이상)
)
select brand, cnt
from agg
where brand !~ '분양'           -- 'OO분양' 류 일반명사 제외(상호 아님)
  -- 일반명사/종(種) 토큰 제외
  and brand not in ('강아지','고양이','애견','반려동물','펫샵','반려','관상어','햄스터')
  -- 지역/모호 토큰 제외
  and brand not in ('서울','부산','대구','인천','광주','대전','울산','수원',
                    '천안','제주','동탄','경기','로얄','행복한','The')
  -- 대형마트 제외(분양 전문 아님 — ⚠ 경고를 유지해야 함)
  and brand not in ('롯데마트','이마트','홈플러스','코스트코','하나로마트')
order by cnt desc;

